package io.levysworks.yetAnotherLootManager.Enums;

/**
 * <p><h2>W.I.P</h2></p>
 * Enum representing the available loot providers for the plugin.
 * <p>Loot providers determine the source of items that can be generated
 * as dungeon loot. Each provider corresponds to a specific integration or system.</p>
 */
public enum Providers {
    /**
     * Represents the Nexo provider, used for generating items from the Nexo system.
     */
    NEXO,

    /**
     * Represents the ItemsAdder provider, used for generating items from the ItemsAdder plugin.
     */
    ITEMSADDER
}
